package classes;

public class Block2048 {
	int number;
	int row;
	int column;
	
	public Block2048(int number, int row, int column) {
		this.number = number;
		this.row = row;
		this.column = column;
	}
	
	public int getNumber() {
		return this.number;
	}
	
	public int getRow() {
		return this.row;
	}
	
	public int column() {
		return this.column;
	}
	
	
}


